/*  ======================================================
    ============= NIL, NULL, NOTHING Y NONE ==============
    ======================================================

    Scala tiene varios tipos "negativos":

    - 'Nil' es la Lista vacía, y es también el terminador de toda Lista

    - 'Null' y su única instancia 'null' es la ausencia de una instancia 'AnyRef'

    - 'Nothing' es un tipo sin instancia, e implica el lanzamiento de una excepción

    - 'None' es la ausencia de un 'Some' en el tipo 'Option', y es una alternativa más segura a 'null'

*/