/*  ======================================================
    ========== MUTABILIDAD VS ESTILO FUNCIONAL ===========
    ======================================================
*/

//  Declaraciones, efectos secundarios, vars y mutabilidad no son estilo de programación funcional.

//  En su lugar, opte por las expresiones, los vals y la inmutabilidad siempre que sea posible.

//  Utilice vars o mutabilidad cuando lo dicte el rendimiento u otros factores.

//  No necesitas una colección var con una mutable, elige una u otra.

//  No deje que la mutabilidad se escape a la API.

//  No optimice el rendimiento antes de tiempo.

//  Mantenga los métodos cortos y sin complicaciones, sepárelos pronto y a menudo.